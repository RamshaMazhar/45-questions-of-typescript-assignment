// Array containing names of favorite pizzas
var favoritePizzas = ["BBQ Pizza", "Chicken Fajita", "Chicken Cheese"];
// Loop through the array and print a statement about each pizza
for (var i = 0; i < favoritePizzas.length; i++) {
    console.log("I like ".concat(favoritePizzas[i], " pizza."));
}
// Print a final statement expressing overall love for pizza
console.log("I really love pizza!");
